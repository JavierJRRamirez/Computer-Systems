/*
 * divpwr2 - Compute x/(2^n), for 0 <= n <= 30
 *  Round toward zero
 *   Examples: divpwr2(15,1) = 7, divpwr2(-33,4) = -2
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int divpwr2(int x, int n) {
	// For x >> n if positive
	// x / 2^n
	int pos = x>>n;
	// round toward 0 and x >> n - 1 if negative
	int neg = (x + (1 << n) + ~0) >> n;
	// want to check the ends to see if -1 or 0 by shifting
	// Negation x is to make the result be 1 for posative
	return (pos & (~x >> 31)) | (neg & (x >> 31));
}

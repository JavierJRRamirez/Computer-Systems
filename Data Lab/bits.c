/*
 * getByte - Extract byte n from word x
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: getByte(0x12345678,1) = 0x56
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int getByte(int x, int n) {
	// n << 3 adjusts n so its value is represented in bit form.
	// x is arithmetically shifted right to n*(2^3) bits to check through first bit.
	// The mask 0xFF is applied to return only the least significant byte
	return (x >> (n << 3)) & 0xFF;
}

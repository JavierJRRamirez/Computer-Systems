/*
 * isPower2 - returns 1 if x is a power of 2, and 0 otherwise
 *   Examples: isPower2(5) = 0, isPower2(8) = 1, isPower2(0) = 0
 *   Note that no negative number is a power of 2.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 60
 *   Rating: 4
 */
int isPower2(int x) {
	// get all 1's in mask. Negate x so that if its a pow of 2, it has only 1 zero.
    // XOR against all 1's mask, since a non-pow-2 would have a diff bit pattern and not
    // come out non-zero
	int mask = ~0;
    return (!(mask ^ (~x + 1)));
}
/*
 * leastBitPos - return a mask that marks the position of the
 *               least significant 1 bit. If x == 0, return 0
 *   Example: leastBitPos(96) = 0x20
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 4
 */
int leastBitPos(int x) {
	// given a number it and & it with it complement gives all zeros
	// add 1 to make the least significant bit a 1
	return (x & (~x + 1));
}
/*
 * bitParity - returns 1 if x contains an odd number of 0's
 *   Examples: bitParity(5) = 0, bitParity(7) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 4
 */
int bitParity(int x) {
	x = x ^ (x>>16); // Spliting the bits in half and using XOR to find the odd bits
    x = x ^ (x>>8);  // XOR extracts the parity bit 
    x = x ^ (x>>4);
    x = x ^ (x>>2);
    x = x ^ (x>>1);
   return x & 1;    // Tells if x is a 1 or 0 by & with 1
   					// x is the parity bit number
}
/*
 * logicalNeg - implement the ! operator, using any of
 *              the legal operators except !
 *   Examples: logicalNeg(3) = 0, logicalNeg(0) = 1
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 4
 */
int logicalNeg(int x) {
   x = x | (x>>16); // Spliting the bits in half and using OR to find the all 0
   x = x | (x>>8);  // OR spots all the zero case
   x = x | (x>>4);
   x = x | (x>>2);
   x = x | (x>>1);
   return ~x & 1;  // complement x so if all zeros turn to 1 and to check if at least one turns to zero 
}
